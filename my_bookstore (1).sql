DROP TABLE book  CASCADE CONSTRAINTS;
DROP TABLE author  CASCADE CONSTRAINTS;
DROP TABLE customer  CASCADE CONSTRAINTS;
DROP TABLE publisher  CASCADE CONSTRAINTS;
DROP TABLE book_category  CASCADE CONSTRAINTS;
DROP TABLE review  CASCADE CONSTRAINTS; 
DROP TABLE book_order  CASCADE CONSTRAINTS;
DROP TABLE shipping_method  CASCADE CONSTRAINTS;


CREATE TABLE customer (
	cust_ident NUMBER (10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	cust_firstname VARCHAR2 (100) DEFAULT NULL,
	cust_lastname VARCHAR2 (100) DEFAULT NULL,
	cust_email VARCHAR2 (100) DEFAULT NULL,
	cust_password VARCHAR2 (9) DEFAULT NULL,
	cust_streetaddress VARCHAR2 (100) DEFAULT NULL,
	cust_city VARCHAR2 (100) DEFAULT NULL,
	cust_state VARCHAR2 (2) DEFAULT NULL,
	cust_zip NUMBER (5) DEFAULT NULL,
	cust_phone VARCHAR2 (12) DEFAULT NULL,
	cust_regdate DATE NOT NULL

);

CREATE TABLE shipping_method (
	ship_ident NUMBER (10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	ship_estdeliv VARCHAR2 (50) NOT NULL,
	ship_type VARCHAR2 (50) NOT NULL,
	ship_cost NUMBER (4, 2) NOT NULL
);

CREATE TABLE author (
	auth_ident NUMBER (10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	auth_firstname VARCHAR2 (100) DEFAULT NULL,
	auth_lastname VARCHAR2 (100) DEFAULT NULL,
	auth_streetaddress VARCHAR2 (100) DEFAULT NULL,
	auth_city VARCHAR2 (100) DEFAULT NULL,
	auth_state VARCHAR2 (2) DEFAULT NULL,
	auth_zip NUMBER (5) DEFAULT NULL,
	auth_email VARCHAR2 (100) DEFAULT NULL,
	auth_phone VARCHAR2 (12) DEFAULT NULL

);

CREATE TABLE publisher (
	pub_ident NUMBER (10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	pub_name VARCHAR2 (255) DEFAULT NULL,
	pub_streetaddress VARCHAR2 (100) DEFAULT NULL,
	pub_city VARCHAR2 (100) DEFAULT NULL,
	pub_state VARCHAR2 (2) DEFAULT NULL,
	pub_zip NUMBER (5) DEFAULT NULL,
	pub_email VARCHAR2 (255) DEFAULT NULL,
	pub_phone VARCHAR2 (12) DEFAULT NULL
    
);

CREATE TABLE book_category (
	cat_ident NUMBER (10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	cat_name VARCHAR2 (100) DEFAULT NULL,
	subcat_name VARCHAR2 (100) DEFAULT NULL
    

);
	
CREATE TABLE book (
	book_ident NUMBER (10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	book_title VARCHAR2 (255) DEFAULT NULL,
	book_genre VARCHAR2 (255) DEFAULT NULL,
	book_price NUMBER (4, 2) DEFAULT NULL,
	book_stock NUMBER (9) DEFAULT NULL,
	book_isbn NUMBER (13) DEFAULT NULL,
	book_pubdate DATE NOT NULL,
	auth_ident NUMBER (10) DEFAULT NULL,
	pub_ident NUMBER (10) DEFAULT NULL,
	cat_ident NUMBER (10) DEFAULT NULL,	
	FOREIGN KEY (cat_ident) REFERENCES book_category (cat_ident) ON DELETE CASCADE,
	FOREIGN KEY (auth_ident) REFERENCES author (auth_ident) ON DELETE CASCADE,
	FOREIGN KEY (pub_ident) REFERENCES publisher (pub_ident) ON DELETE CASCADE 
);

CREATE TABLE review (
	review_ident NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	review_rate NUMBER (1) DEFAULT NULL,
	review_comment VARCHAR2 (500) DEFAULT NULL,
	review_date DATE NOT NULL,
    book_ident NUMBER (10) NOT NULL,
    cust_ident NUMBER (10) NOT NULL,
	FOREIGN KEY (book_ident) REFERENCES book (book_ident) ON DELETE CASCADE,
	FOREIGN KEY (cust_ident) REFERENCES customer (cust_ident) ON DELETE CASCADE
);

CREATE TABLE book_order (
	order_ident NUMBER (10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	order_date DATE NOT NULL,
	order_shipaddress VARCHAR2 (255) NOT NULL,
 	order_status VARCHAR2 (20) NOT NULL,
    ship_ident NUMBER (10) NOT NULL,
    cust_ident NUMBER (10) NOT NULL,
    book_ident NUMBER (10) NOT NULL,
	FOREIGN KEY (ship_ident) REFERENCES shipping_method (ship_ident) ON DELETE CASCADE,
	FOREIGN KEY (cust_ident) REFERENCES customer (cust_ident) ON DELETE CASCADE,
	FOREIGN KEY (book_ident) REFERENCES book (book_ident) ON DELETE CASCADE
);



