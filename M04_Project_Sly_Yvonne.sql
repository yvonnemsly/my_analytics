CREATE TABLE CUSTOMER
(CUSTOMER_ID NUMBER (10) CONSTRAINT CUSTOMER_CUSTOMER_ID_NN NOT NULL,
LAST_NAME VARCHAR2 (25) CONSTRAINT CUSTOMER_LAST_NAME_NN NOT NULL, 
FIRST_NAME VARCHAR2 (25) CONSTRAINT CUSTOMER_FIRST_NAME_NN NOT NULL,
HOME_PHONE VARCHAR2 (12) CONSTRAINT CUSTOMER_HOME_PHONE_NN NOT NULL,
ADDRESS VARCHAR2 (100) CONSTRAINT CUSTOMER_ADDRESS_NN NOT NULL,
CITY VARCHAR2 (30) CONSTRAINT CUSTOMER_CITY_NN NOT NULL,
STATE VARCHAR2 (2) CONSTRAINT CUSTOMER_STATE_NN NOT NULL,
EMAIL VARCHAR2 (25),
CELL_PHONE VARCHAR2 (12));
CREATE TABLE ACTORS
(ACTOR_ID NUMBER (10) CONSTRAINT ACTORS_ACTOR_ID_NN NOT NULL,
STAGE_NAME VARCHAR2 (40) CONSTRAINT ACTORS_STAGE_NAME_NN NOT NULL,
FIRST_NAME VARCHAR2 (25) CONSTRAINT ACTORS_FIRST_NAME_NN NOT NULL,
LAST_NAME VARCHAR2 (25) CONSTRAINT ACTORS_LAST_NAME_NN NOT NULL,
BIRTH_DATE DATE CONSTRAINT ACTORS_BIRTH_DATE_NN NOT NULL);
CREATE TABLE MOVIES
(TITLE_ID NUMBER (10) CONSTRAINT MOVIES_TITLE_ID_NN NOT NULL,
TITLE VARCHAR2 (60) CONSTRAINT MOVIES_TITLE_NN NOT NULL,
DESCRIPTION VARCHAR2 (400) CONSTRAINT MOVIES_DESCRIPTION_NN NOT NULL,
RATING VARCHAR2 (4),
CATEGORY VARCHAR2 (20),
RELEASE_DATE DATE CONSTRAINT MOVIES_RELEASE_DATE_NN NOT NULL);
CREATE TABLE STAR_BILLINGS
(ACTOR_ID NUMBER (10) CONSTRAINT STAR_BILLINGS_ACTOR_ID_NN NOT NULL,
TITLE_ID NUMBER (10) CONSTRAINT STAR_BILLINGS_TITLE_ID_NN NOT NULL,
"COMMENTS" VARCHAR2 (40));
CREATE TABLE MEDIA
(MEDIA_ID NUMBER (10) CONSTRAINT MEDIA_MEDIA_ID_NN NOT NULL,
FORMAT VARCHAR2 (3) CONSTRAINT MEDIA_FORMAT_NN NOT NULL,
TITLE_ID NUMBER (10) CONSTRAINT MEDIA_TITLE_ID_NN NOT NULL);
CREATE TABLE RENTAL_HISTORY
(MEDIA_ID NUMBER (10) CONSTRAINT RENTAL_HISTORY_MEDIA_ID_NN NOT NULL,
RENTAL_DATE DATE CONSTRAINT RENTAL_HISTORY_RENTAL_DATE_NN NOT NULL,
CUSTOMER_ID NUMBER (10) CONSTRAINT RENTAL_HISTORY_CUSTOMER_ID_NN NOT NULL,
return_DATE DATE);
ALTER TABLE CUSTOMER ADD CONSTRAINT PK_CUSTOMER PRIMARY KEY (CUSTOMER_ID);
ALTER TABLE ACTORS ADD CONSTRAINT PK_ACTORS PRIMARY KEY (ACTOR_ID);
ALTER TABLE MOVIES ADD CONSTRAINT PK_MOVIES PRIMARY KEY (TITLE_ID);
ALTER TABLE STAR_BILLINGS ADD CONSTRAINT PK_STAR_BILLINGS PRIMARY KEY (ACTOR_ID, TITLE_ID);
ALTER TABLE MEDIA ADD CONSTRAINT PK_MEDIA PRIMARY KEY (MEDIA_ID);
ALTER TABLE RENTAL_HISTORY ADD CONSTRAINT PK_RENTAL_HISTORY PRIMARY KEY (MEDIA_ID, RENTAL_DATE);
ALTER TABLE MEDIA ADD CONSTRAINT FK_MED_MOV FOREIGN KEY (TITLE_ID) REFERENCES MOVIES(TITLE_ID);
ALTER TABLE RENTAL_HISTORY ADD CONSTRAINT FK_REN_HIS_MED FOREIGN KEY (MEDIA_ID) REFERENCES MEDIA(MEDIA_ID);
ALTER TABLE RENTAL_HISTORY ADD CONSTRAINT FK_REN_HIS_CUST FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID);
ALTER TABLE MOVIES ADD CONSTRAINT chk_RATING CHECK (RATING IN ('G','PG','PG13','R'));
ALTER TABLE MOVIES ADD CONSTRAINT chk_category CHECK (CATEGORY IN ('Drama','Comedy','Action','Child','SCIFI','Documentary'));
